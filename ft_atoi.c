#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	v;
	int	neg;

	neg = 1;
	i = 0;
	while (s[i] == ' ' || (s[i] >= '\t' && s[i] <= '\r'))
		i++;
	neg *= 1 - 2 * (s[i] == '-');
	i += 1 * (s[i] == '-' || s[i] == '+');
	v = 0;
	while ('0' <= s[i] && s[i] <= '9')
		v = v * 10 + s[i++] - '0';
	return (v * neg);
}
